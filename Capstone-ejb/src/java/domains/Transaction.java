package domains;

/**
 *
 * @author sebaslab
 */
public class Transaction {
    
    private int idTransaction;
    private String dateOfPurchase;
    private int[] listOfPhotos;
    private int idMember;

    /**
     *
     */
    public Transaction() {
    }

    /**
     *
     * @param idTransaction
     * @param dateOfPurchase
     * @param listOfPhotos
     * @param idMember
     */
    public Transaction(int idTransaction, String dateOfPurchase, int[] listOfPhotos, int idMember) {
        this.idTransaction = idTransaction;
        this.dateOfPurchase = dateOfPurchase;
        this.listOfPhotos = listOfPhotos;
        this.idMember = idMember;
    }

    /**
     *
     * @return
     */
    public int getIdTransaction() {
        return idTransaction;
    }

    /**
     *
     * @param idTransaction
     */
    public void setIdTransaction(int idTransaction) {
        this.idTransaction = idTransaction;
    }

    /**
     *
     * @return
     */
    public String getDateOfPurchase() {
        return dateOfPurchase;
    }

    /**
     *
     * @param dateOfPurchase
     */
    public void setDateOfPurchase(String dateOfPurchase) {
        this.dateOfPurchase = dateOfPurchase;
    }

    /**
     *
     * @return
     */
    public int[] getListOfPhotos() {
        return listOfPhotos;
    }

    /**
     *
     * @param listOfPhotos
     */
    public void setListOfPhotos(int[] listOfPhotos) {
        this.listOfPhotos = listOfPhotos;
    }

    /**
     *
     * @return
     */
    public int getIdMember() {
        return idMember;
    }

    /**
     *
     * @param idMember
     */
    public void setIdMember(int idMember) {
        this.idMember = idMember;
    }

    @Override
    public String toString() {
        return "Transaction{" + "idTransaction=" + idTransaction + ", dateOfPurchase=" + dateOfPurchase + ", listOfPhotos=" + listOfPhotos + ", idMember=" + idMember + '}';
    }
    
}
